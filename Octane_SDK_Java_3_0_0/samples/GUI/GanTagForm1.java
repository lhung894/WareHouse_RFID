    private void jBtnNhapActionPerformed(java.awt.event.ActionEvent evt)//GEN-FIRST:event_jBtnNhapActionPerformed
    {//GEN-HEADEREND:event_jBtnNhapActionPerformed
        // TODO add your handling code here:
        System.out.println("GẮN TAG AUTO!!!!");
        ArrayList<RFIDTagDTO> listAuto = new ArrayList<>();
        try
        {
            listAuto = NhapExcel.getList();
        } catch (IOException ex)
        {
            System.out.println("Lôi Nhap Auto " + ex.getMessage());
        }

        for (RFIDTagDTO tagDTO : listAuto)
        {
            System.out.println(tagDTO);
            if (tagBUS.insertTag(tagDTO))
            {
                System.out.println("====>Thêm auto Tag đa ok!!!");
                SanPhamDTO sanPhamDTO = new SanPhamDTO();
                sanPhamDTO.setProductId((String) tagDTO.getProductId());
                int quantity = tagBUS.countSanPhamId(tagDTO.getProductId().toString());
                System.out.println("So luong la: " + quantity);
                sanPhamDTO.setProductQuantity((Integer) quantity);
                System.out.println("Quantity cua SAnphamDTO moi ne!!: " + sanPhamDTO.getProductQuantity());
                if (sanPhamBUS.updateProduct(sanPhamDTO))
                {
                    System.out.println("Thêm quantity OK!!!1");
                }
            } else
            {
                JOptionPane.showMessageDialog(this, "Gán Tag Tự Động Thất Bại!!!!");
            }
        }
        createTableSanPham();

    }//GEN-LAST:event_jBtnNhapActionPerformed

