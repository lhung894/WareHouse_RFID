    private void jBtnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtnAddActionPerformed
        // TODO add your handling code here:
        RFIDTagDTO tagDTO = new RFIDTagDTO(idTag, idProduct, "", null, "", null, "");
        RFIDTagDTO tagTempDTO = new RFIDTagDTO();

        for (RFIDTagDTO tag : tagList)
        {
            if (tag.getTagId().equals(idTag))
            {
                tagDTO.setTagGateIn(tag.getTagGateIn());
                tagDTO.setTagDateIn(tag.getTagDateIn());
                tagTempDTO = tag;
                break;
            }
        }

        if (tagBUS.insertTag(tagDTO))
        {
            SanPhamDTO sanPhamDTO = new SanPhamDTO();
            sanPhamDTO.setProductId((String) tbModelSanPham.getValueAt(rowProduct, 0));
            sanPhamDTO.setProductQuantity((Integer) tbModelSanPham.getValueAt(rowProduct, 2) + 1);

            if (sanPhamBUS.updateProduct(sanPhamDTO))
            {
                jTableSanPham.setValueAt((Integer) tbModelSanPham.getValueAt(rowProduct, 2) + 1, rowProduct, 2);
                updateTableModelTag();
                JOptionPane.showMessageDialog(this, "Gán tag Ok!!!!");
                jTableTag.clearSelection();
                jTableSanPham.clearSelection();
                rowProduct = -2;
                rowTag = -2;
                selectedTag = false;
                selectedProduct = false;
                jBtnAdd.setEnabled(false);
                if (tagList.remove(tagTempDTO))
                {
                    System.out.println("OK!!! Xoa");
                } else
                {
                    System.out.println("Not xoa!!");
                }
            }
        } else
        {
            JOptionPane.showMessageDialog(this, "Gán tag thất bại!!!!");
        }
    }//GEN-LAST:event_jBtnAddActionPerformed

