    public void creatTableModelTag(DefaultTableModel model)
    {
        for (RFIDTagDTO tag : tagList)
        {
            System.out.println("tag: " + tag);
            Vector row = new Vector();
            row.add(tag.getTagId());
            model.addRow(row);
        }
    }

    public void createTableTag()
    {
        tbModelTag.setRowCount(0);
        creatTableModelTag(tbModelTag);
        jTableTag.setRowSorter(null);
        jTableTag.setAutoCreateRowSorter(true);
        jTableTag.setModel(tbModelTag);
        jTableTag.setSelectionMode(ListSelectionModel.SINGLE_SELECTION);
    }

    public void createTagAuto()
    {
        tbModelTag.setRowCount(0);
        creatTableModelTag(tbModelTag);
    }

    public void createTableModelSanPham(DefaultTableModel model)
    {
        sanPhamBUS = new SanPhamBUS();
        for (SanPhamDTO sanPham : sanPhamBUS.getList())
        {
            Vector row = new Vector();
            row.add(sanPham.getProductId());
            row.add(sanPham.getProductName());
            row.add(sanPham.getProductQuantity());
            row.add(sanPham.getProductDetail());
            model.addRow(row);
        }
    }

    public void createTableSanPham()
    {
        clear();
        tbModelSanPham.setRowCount(0);
        createTableModelSanPham(tbModelSanPham);
        jTableSanPham.setModel(tbModelSanPham);
        jTableSanPham.setSelectionMode(ListSelectionModel.SINGLE_SELECTION);
    }

    void clear()
    {
        rowProduct = -2;
        rowTag = -2;
        tbModelTag.setRowCount(0);
        jTableTag.clearSelection();
        jTableSanPham.clearSelection();
        selectedTag = false;
        selectedProduct = false;
        jBtnAdd.setEnabled(false);
        if (!tagList.isEmpty())
        {
            tagList.clear();
        }
    }

    public void updateTableModelTag()
    {
        for (int i = 0; i < tbModelTag.getRowCount(); i++)
        {
            if (idTag.equals(tbModelTag.getValueAt(i, 0)))
            {
                System.out.println("remove");
                tbModelTag.removeRow(i);
                break;
            }
        }
    }

